public class TriangleTwo
{

	public static String go( String let, int size)
	{
	int i = size;
        int j = 1;

	for(;i >= 0; i--)
        {
            for(j=1;j <= i; j++)
            {
                System.out.print(let);

            }
            System.out.print("\n");
        }


	String output="";
	return output;
	}
}
